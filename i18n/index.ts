// AsyncStorage key for language persistence
const LANGUAGE_KEY = 'app-language';

// Function to get stored language
const getStoredLanguage = async (): Promise<string> => {
  try {
    const stored = await AsyncStorage.getItem(LANGUAGE_KEY);
    return stored || 'en-US';
  } catch {
    return 'en-US';
  }
};

// Function to set stored language
export const setStoredLanguage = async (language: string): Promise<void> => {
  try {
    await AsyncStorage.setItem(LANGUAGE_KEY, language);
  } catch (error) {
    console.error('Failed to store language:', error);
  }
};

// Initialize i18n synchronously with default language, then update if stored language exists
i18n
  .use(initReactI18next)
  .init({
    resources: JSON.parse(JSON.stringify(resources)),
    lng: 'en-US', // Default to English, will be updated if stored language exists
    fallbackLng: 'en-US',
    interpolation: {
      escapeValue: false,
    },
  });

// Update language from storage after initialization
getStoredLanguage().then((storedLang) => {
  if (storedLang && storedLang !== 'en-US') {
    i18n.changeLanguage(storedLang);
  }
});

export default i18n;


